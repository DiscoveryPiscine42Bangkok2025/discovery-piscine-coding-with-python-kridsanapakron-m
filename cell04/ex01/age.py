#!/usr/bin/env python3
def main():
    input_age = int(input("Please tell me your age: "))
    print("You are currently " + str(input_age) + " years old.")
    print("In 10 years, you'll be " + str(input_age + 10) + " years old.")
    print("In 20 years, you'll be " + str(input_age + 20) + " years old.")
    print("In 30 years, you'll be " + str(input_age + 30) + " years old.")
main()